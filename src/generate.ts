/**
 * The generation pipeline: turn a {@link ServerSpec} into an in-memory set of
 * files ({@link GeneratedFile}[]) for a production-shaped MCP server.
 *
 * This module is pure (no filesystem side effects) so it can be unit-tested by
 * inspecting the generated strings; {@link scaffold} in scaffold.ts writes the
 * result to disk. The generated project depends only on Node built-ins and
 * implements the MCP 2025-06-18 JSON-RPC methods over the stdio transport:
 *
 *   initialize, notifications/initialized, ping,
 *   tools/list, tools/call,
 *   resources/list, resources/read, resources/templates/list,
 *   prompts/list, prompts/get
 *
 * Every generated `.mjs` uses Node built-ins only, `\n`-delimited JSON-RPC
 * framing (no embedded newlines), and writes only valid MCP messages to stdout
 * (logs go to stderr). Original Cognis Digital implementation.
 */

import { MCP_PROTOCOL_VERSION } from "./types.js";
import type {
  PromptDefinition,
  ResourceDefinition,
  ResourceTemplateDefinition,
  ServerSpec,
  ToolDefinition,
} from "./types.js";

/** A single generated file: repo-relative POSIX path + UTF-8 contents. */
export interface GeneratedFile {
  path: string;
  contents: string;
}

/** Convert a primitive name to a safe JS identifier for handler wiring. */
function ident(name: string): string {
  const id = name.replace(/[^A-Za-z0-9_$]/g, "_");
  return /^[A-Za-z_$]/.test(id) ? id : `_${id}`;
}

/** Convert a URI/template to a safe filename fragment. */
function slug(s: string): string {
  return s.replace(/[^A-Za-z0-9._-]+/g, "_").replace(/^_+|_+$/g, "") || "item";
}

/**
 * Build a minimal, schema-valid arguments object for a tool's required inputs.
 * Integers/numbers get `1`, booleans get `true`, everything else gets
 * `stringSample`. Shared by the generated test and smoke-script emitters so both
 * exercise the tool with the same (valid) call shape.
 */
function sampleArgsForTool(
  tool: ToolDefinition | undefined,
  stringSample: string
): Record<string, unknown> {
  const args: Record<string, unknown> = {};
  const props = tool?.inputSchema?.properties ?? {};
  for (const req of tool?.inputSchema?.required ?? []) {
    const p = props[req];
    const t = Array.isArray(p?.type) ? p?.type[0] : p?.type;
    args[req] =
      t === "integer" || t === "number" ? 1 : t === "boolean" ? true : stringSample;
  }
  return args;
}

const HEADER = "// Generated by mcpscaffold. Original Cognis Digital implementation.\n";

/** Emit `export default <json>` so handlers/specs can be imported directly. */
function jsonModule(value: unknown, exportName = "default"): string {
  const kw = exportName === "default" ? "export default" : `export const ${exportName} =`;
  return `${HEADER}${kw} ${JSON.stringify(value, null, 2)};\n`;
}

