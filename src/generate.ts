/**
 * Generate the full file set for a server described by `spec`. Pure: returns
 * files in memory without touching the filesystem.
 */
export function generateServerFiles(spec: ServerSpec): GeneratedFile[] {
  const files: GeneratedFile[] = [];

  files.push({ path: "spec.json", contents: JSON.stringify(spec, null, 2) + "\n" });
  files.push({
    path: "tools.json",
    contents: JSON.stringify(spec.tools ?? [], null, 2) + "\n",
  });

  files.push(transportFile());
  files.push(authFile(spec));
  files.push(serverFile(spec));

  for (const t of spec.tools ?? []) files.push(toolHandlerFile(t));
  for (const r of spec.resources ?? []) files.push(resourceHandlerFile(r));
  for (const t of spec.resourceTemplates ?? []) files.push(resourceTemplateHandlerFile(t));
  for (const p of spec.prompts ?? []) files.push(promptHandlerFile(p));

  files.push(packageJsonFile(spec));
  files.push(serverTestFile(spec));
  files.push(smokeScriptFile(spec));
  files.push(readmeFile(spec));
  files.push({ path: ".gitignore", contents: "node_modules/\n*.log\n" });

  return files;
}
