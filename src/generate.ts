function smokeScriptFile(spec: ServerSpec): GeneratedFile {
  const firstTool = (spec.tools ?? [])[0];
  const args = sampleArgsForTool(firstTool, "hello");
  return {
    path: "scripts/smoke.mjs",
    contents:
      HEADER +
      `// End-to-end smoke test: spawn the server, send initialize + tools/call\n` +
      `// over stdio, and assert on valid MCP responses. Exits 0 on success.\n` +
      `import { spawn } from "node:child_process";\n` +
      `import { fileURLToPath } from "node:url";\n` +
      `import { dirname, join } from "node:path";\n` +
      `\n` +
      `const here = dirname(fileURLToPath(import.meta.url));\n` +
      `const serverPath = join(here, "..", "src", "server.mjs");\n` +
      `\n` +
      `const child = spawn(process.execPath, [serverPath], { stdio: ["pipe", "pipe", "inherit"] });\n` +
      `let buffer = "";\n` +
      `const pending = new Map();\n` +
      `child.stdout.on("data", (chunk) => {\n` +
      `  buffer += chunk.toString("utf8");\n` +
      `  let nl;\n` +
      `  while ((nl = buffer.indexOf("\\n")) >= 0) {\n` +
      `    const line = buffer.slice(0, nl).trim();\n` +
      `    buffer = buffer.slice(nl + 1);\n` +
      `    if (!line) continue;\n` +
      `    const msg = JSON.parse(line);\n` +
      `    const resolve = pending.get(msg.id);\n` +
      `    if (resolve) { pending.delete(msg.id); resolve(msg); }\n` +
      `  }\n` +
      `});\n` +
      `function send(msg) {\n` +
      `  return new Promise((resolve) => {\n` +
      `    pending.set(msg.id, resolve);\n` +
      `    child.stdin.write(JSON.stringify(msg) + "\\n");\n` +
      `  });\n` +
      `}\n` +
      `function fail(m) { console.error("SMOKE FAIL: " + m); child.kill(); process.exit(1); }\n` +
      `\n` +
      `const init = await send({ jsonrpc: "2.0", id: 1, method: "initialize", params: { protocolVersion: "${MCP_PROTOCOL_VERSION}", capabilities: {}, clientInfo: { name: "smoke", version: "0" } } });\n` +
      `if (!init.result || !init.result.serverInfo) fail("initialize did not return serverInfo");\n` +
      `console.error("initialize ok: " + init.result.serverInfo.name + " " + init.result.serverInfo.version);\n` +
      `\n` +
      `const listed = await send({ jsonrpc: "2.0", id: 2, method: "tools/list" });\n` +
      `if (!Array.isArray(listed.result.tools)) fail("tools/list did not return an array");\n` +
      `console.error("tools/list ok: " + listed.result.tools.length + " tool(s)");\n` +
      (firstTool
        ? `const called = await send({ jsonrpc: "2.0", id: 3, method: "tools/call", params: { name: ${JSON.stringify(
            firstTool.name
          )}, arguments: ${JSON.stringify(args)} } });\n` +
          `if (!called.result || !Array.isArray(called.result.content)) fail("tools/call did not return content");\n` +
          `console.error("tools/call ok: " + JSON.stringify(called.result.content[0]));\n`
        : "") +
      `\n` +
      `child.stdin.end();\n` +
      `child.kill();\n` +
      `console.error("SMOKE OK");\n` +
      `process.exit(0);\n`,
  };
}

